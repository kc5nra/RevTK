<h2>Quick Tips</h2>

<div class="section">
	<p> You can <strong>search</strong> stories by entering a frame number, a keyword, or the
		corresponding Japanese character.
	</p>
	<p> The <strong>Learned</strong> button appears for those stories for which you have
		failed the kanji review. Click the button to indicate that you have re-studied
		the character : the failed kanji will return to the review cycle, it will appear
		in the second box, and will first expire in about 3 days.
	</p>
</div>

<h2>Sharing hints and stories</h2>

<div class="section">
	<p> You can share either <strong>hints</strong> or <strong>stories</strong>.
	</p>
	<p> <strong>Hints</strong> are things like images, characters, references to history, literature, and such,
		which can help other members to create their own stories. An example would be "The
		Addams Family" for a kanji containing the 'graveyard' primitive.
	</p>
	<p> <strong>Stories</strong> are detailed mnemonics, such as those that James Heisig provides in the
		first Chapter of RTK. Please note the difference between a mnemonic and a story !
		Mnemonics are little more than a list of primitives and will generally make sense only
		to the person who created them. One may think of some food as horrible while others will 
		think of it as delicious. By writing a story, you are adding details to your mnemonics
		that help others understand what you had in mind.
	</p>
	<p> <strong>Keywords</strong> are automatically highlighted in stories and displayed in a <strong>bold</strong> type.
		If the keyword is spelled differently, enclose the keyword with hashes, like <strong>#</strong>this<strong>#</strong>.
	</p>
	<p> <strong>Primitives</strong> can be formatted in your stories by enclosing them with the star character
		*like this*. They will then appear <i>like this</i>.
	</p>
	<p> <strong>Links between stories</strong> can be created easily by typing in the frame number enclosed 
		with braces. For example <strong>{</strong>113<strong>}</strong> will show up in stories as <em>stone</em> (<a href="?framenum=113">frame 113)</a>.
	</p>
</div>

